list.files('.', 'imp*')
data <- list()
for (i in list.files('.', 'imp*')) {
	print(i)
    name <- sub('imp_', '', i)
	print(name)
	name <- sub('_.*', '', name)
	print(name)
	imps <- read.table(i, header=T)
	#print(head(imps))
	print(dim(imps))
	data[[name]] <- imps
}

for (i in names(data)) cat ('\t',i)
cat('\n')
for (i in names(data)) {
    cat(i, '\t', sep='')
	for (j in names(data)) {
		cat(i, 'x', j,':', sum(data[[i]][,1] %in% data[[j]][,1]), '\t', sep='')
	}
	cat('\n')
}


